"""Gap diagnosis (classification: SCATTER_REDUCE): this oracle zero-initializes the returned dense vocabulary-gradient tensor and scatters each live hidden vector directly from `arg1_1` into duplicate token-id buckets while folding the `arg0_1 == 1` mask into the scatter, whereas Inductor currently materializes the identity multiply, broadcast equality/where producer, full zero base, and generic `index_put(accumulate=True)` as separate scheduled work; Inductor cannot do this today because scheduler/codegen lacks a row-index scatter-add lowering that fuses a broadcast mask/value producer into the atomic embedding-gradient update while preserving duplicate accumulation; the fix is SCATTER_REDUCE: add a structured `index_put(accumulate=True)` lowering for embedding-gradient rows that sinks simple pointwise producers into the scatter and emits the zero-fill plus row atomic-add without materializing the `[B, S, H]` update tensor."""
from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any

import torch

try:
    import triton
    import triton.language as tl
except ImportError:  # pragma: no cover - keeps py_compile usable without Triton.
    triton = None
    tl = None

from oracle_harness import (
    oracle_impl,
    bench_oracle,
    bench_oracle_all_shapes,
    check_oracle,
    get_hardware_info,
    get_inputs as _harness_get_inputs,
    get_repro_instance as _harness_get_repro_instance,
    has_stochastic_ops,
)


REPRO_DIR = Path(__file__).resolve().parent
REPRO_ID = REPRO_DIR.name
REPRO_PATH = REPRO_DIR / "repro.py"

VOCAB = 50265
HIDDEN = 1024
BLOCK_HIDDEN = 1024
SCATTER_WARPS = 8


def get_inputs() -> tuple[Any, ...]:
    """Load inputs from the repro's make_inputs (scope invariant: same inputs)."""
    return tuple(_harness_get_inputs(REPRO_DIR))


def get_repro_instance() -> torch.nn.Module:
    """Create a Repro() instance for reference comparison."""
    return _harness_get_repro_instance(REPRO_DIR)


if triton is not None:

    @triton.jit
    def _masked_embedding_scatter_kernel(
        values_ptr,
        index_ptr,
        out_ptr,
        tokens: tl.constexpr,
        vocab: tl.constexpr,
        hidden: tl.constexpr,
        BLOCK_H: tl.constexpr,
    ):
        token = tl.program_id(0)
        hidden_block = tl.program_id(1)
        h = hidden_block * BLOCK_H + tl.arange(0, BLOCK_H)
        h_mask = h < hidden

        raw_index = tl.load(index_ptr + token).to(tl.int64)
        wrapped_index = tl.where(raw_index < 0, raw_index + vocab, raw_index)
        live = (raw_index != 1) & (wrapped_index >= 0) & (wrapped_index < vocab)

        values = tl.load(
            values_ptr + token * hidden + h,
            mask=h_mask & live,
            other=0.0,
        ).to(tl.float32)
        tl.atomic_add(
            out_ptr + wrapped_index * hidden + h,
            values,
            sem="relaxed",
            mask=h_mask & live & (token < tokens),
        )


def _validate_inputs(inputs: tuple[Any, ...]) -> tuple[torch.Tensor, torch.Tensor]:
    if len(inputs) != 2:
        raise ValueError(f"expected 2 inputs, got {len(inputs)}")
    arg1_1, arg0_1 = inputs

    if not isinstance(arg1_1, torch.Tensor) or not isinstance(arg0_1, torch.Tensor):
        raise TypeError("oracle inputs must be tensors")
    if arg1_1.device.type != "cuda" or arg0_1.device.type != "cuda":
        raise RuntimeError("Triton oracle requires CUDA inputs")
    if arg1_1.dtype is not torch.float32:
        raise ValueError(f"arg1_1 expected torch.float32, got {arg1_1.dtype}")
    if arg0_1.dtype is not torch.int64:
        raise ValueError(f"arg0_1 expected torch.int64, got {arg0_1.dtype}")
    if arg1_1.ndim != 3 or arg0_1.ndim != 2:
        raise ValueError(
            f"expected arg1_1 rank 3 and arg0_1 rank 2, got {arg1_1.ndim} and {arg0_1.ndim}"
        )
    if tuple(arg1_1.shape[:2]) != tuple(arg0_1.shape):
        raise ValueError(
            f"leading shapes must match, got arg1_1={tuple(arg1_1.shape)} arg0_1={tuple(arg0_1.shape)}"
        )
    if arg1_1.shape[2] != HIDDEN:
        raise ValueError(f"expected hidden size {HIDDEN}, got {arg1_1.shape[2]}")
    if not arg1_1.is_contiguous() or not arg0_1.is_contiguous():
        raise ValueError("oracle expects contiguous inputs generated by the repro harness")
    return arg1_1, arg0_1


@oracle_impl(hardware="H100", shapes="(T([8, 1024, 1024], f32), T([8, 1024], i64, gen=Index(50265)))")
def oracle_forward(inputs: tuple[Any, ...] | list[Any]) -> torch.Tensor:
    """Run the full returned-output computation for Repro.forward."""
    if triton is None:
        raise RuntimeError("triton is required for this oracle")

    values, indices = _validate_inputs(tuple(inputs))
    tokens = indices.numel()

    out = torch.empty((VOCAB, HIDDEN), device=values.device, dtype=torch.float32)
    out.zero_()
    _masked_embedding_scatter_kernel[(tokens, triton.cdiv(HIDDEN, BLOCK_HIDDEN))](
        values,
        indices,
        out,
        tokens=tokens,
        vocab=VOCAB,
        hidden=HIDDEN,
        BLOCK_H=BLOCK_HIDDEN,
        num_warps=SCATTER_WARPS,
    )
    return out


def main() -> None:
    parser = argparse.ArgumentParser(
        description=f"Oracle for {REPRO_ID}",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--check", action="store_true", help="Verify correctness against eager Repro")
    parser.add_argument("--bench", action="store_true", help="Benchmark oracle vs torch.compile")
    parser.add_argument("--rtol", type=float, default=1e-2, help="Relative tolerance for correctness check")
    parser.add_argument("--atol", type=float, default=1e-2, help="Absolute tolerance for correctness check")
    parser.add_argument("--warmup", type=int, default=25, help="Warmup iterations for benchmark")
    parser.add_argument("--rep", type=int, default=200, help="Repetitions for benchmark")
    parser.add_argument(
        "--no-skip-stochastic",
        action="store_true",
        help="Disable auto-detection and skipping of stochastic outputs",
    )
    parser.add_argument("--all-shapes", action="store_true", help="Benchmark across all shapes from shapes.txt")
    parser.add_argument("--show-hw", action="store_true", help="Print GPU hardware info and exit")
    args = parser.parse_args()

    if args.show_hw:
        import json

        print(json.dumps(get_hardware_info(), indent=2))
        return

    if not args.check and not args.bench:
        args.check = args.bench = True

    inputs = get_inputs()
    instance = get_repro_instance()

    if has_stochastic_ops(REPRO_PATH):
        print(f"NOTE: {REPRO_ID} contains stochastic ops; affected outputs will be auto-skipped")

    if args.check:
        print(f"Checking {REPRO_ID}...")
        ok = check_oracle(
            oracle_forward,
            instance,
            inputs,
            atol=args.atol,
            rtol=args.rtol,
            skip_stochastic=not args.no_skip_stochastic,
        )
        print(f"Correctness: {'PASS' if ok else 'FAIL'}")
        if not ok:
            sys.exit(1)

    if args.bench:
        print(f"Benchmarking {REPRO_ID}...")
        if args.all_shapes:
            results = bench_oracle_all_shapes(
                oracle_forward,
                REPRO_DIR,
                REPRO_ID,
                warmup=args.warmup,
                rep=args.rep,
            )
            for result in results:
                if result["status"] == "BAD_ORACLE":
                    print(
                        f"WARNING: oracle is slower than compile for "
                        f"{result['repro_id']} (ratio={result['ratio']:.3f}x)"
                    )
        else:
            result = bench_oracle(
                oracle_forward,
                instance,
                inputs,
                REPRO_ID,
                warmup=args.warmup,
                rep=args.rep,
            )
            if result["status"] == "BAD_ORACLE":
                print(f"WARNING: oracle is slower than compile (ratio={result['ratio']:.3f}x)")


if __name__ == "__main__":
    main()
