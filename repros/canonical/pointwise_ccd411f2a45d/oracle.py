@triton.jit
def _f32_to_bf16_storage_kernel(input_ptr, output_ptr, n_elements: tl.constexpr, BLOCK: tl.constexpr):
    offsets = tl.program_id(0) * BLOCK + tl.arange(0, BLOCK)
    mask = offsets < n_elements
    values = tl.load(input_ptr + offsets, mask=mask, other=0.0)
    tl.store(
        output_ptr + offsets,
        values.to(tl.bfloat16, fp_downcast_rounding="rtne"),
        mask=mask,
    )


