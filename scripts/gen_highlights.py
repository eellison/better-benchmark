#!/usr/bin/env python3
"""Generate a 4-tier perf-highlights markdown doc from an A/B, reproducibly.

Widest to narrowest, each tier in its own unit:
  Tier 4  all models   -- per-model-e2e geomean/median over the corpus (headline)
  Tier 3  suites       -- per-model-e2e geomean within each suite x mode
  Tier 2  models       -- top individual per-model-e2e movers
  Tier 1  kernels      -- per-fused-kernel speedup (NOT a model number)

genai is EXCLUDED from the model/suite/all-models geomeans (a genai entry is a
single microbenchmark kernel, not a model); genai's wins are listed under the
kernel tier where they belong.

Inputs:
  --base / --head : the two full-corpus sweep JSONs (bench_parallel output;
                    per (repro, model-shape) with compiled_us/coord_descent_us).
                    Used for the KERNEL tier (per-pattern speedups) directly.
  --rollup        : a perf_ab_rollup.py --json output (per-model-e2e dict).
                    Used for tiers 4/3/2. (perf_ab_rollup consumes the same two
                    sweeps + the occurrence accounting; pass its --json here.)

Usage:
  python scripts/perf_ab_rollup.py --base B.json --head H.json --json > rollup.json
  python scripts/gen_highlights.py --base B.json --head H.json --rollup rollup.json \\
      --title "inductor perf branch (base 5e2ab -> HEAD daa79)" --out HIGHLIGHTS.md
"""
from __future__ import annotations

import argparse
import json
import statistics
from collections import defaultdict


def _sweep_points(path):
    d = json.load(open(path))
    out = {}
    for repro, models in d.items():
        if repro.startswith("_") or not isinstance(models, dict):
            continue
        fam = repro.split("/")[-2] if "/" in repro else repro
        for mt, pt in models.items():
            if isinstance(pt, dict):
                us = pt.get("coord_descent_us") or pt.get("compiled_us")
                if us:
                    out[(fam, mt)] = us
    return out


def _geo_pct(pcts):
    finite = [p for p in pcts if p is not None]
    ratios = [1.0 / (1.0 - p / 100.0) for p in finite if (1.0 - p / 100.0) > 0]
    if len(ratios) != len(finite):
        # a model posted a >=100% "improvement" -> ratio would be non-positive.
        # Surface it rather than silently geomean over a smaller set than reported.
        import sys
        print(f"WARNING: {len(finite) - len(ratios)} model(s) with pct>=100%% "
              f"dropped from geomean (n mismatch — geomean over {len(ratios)}, not {len(finite)})",
              file=sys.stderr)
    return (statistics.geometric_mean(ratios) - 1.0) * 100.0 if ratios else None


def main():
    ap = argparse.ArgumentParser(formatter_class=argparse.RawDescriptionHelpFormatter,
                                 epilog=__doc__)
    ap.add_argument("--base", required=True, help="base sweep JSON")
    ap.add_argument("--head", required=True, help="head sweep JSON")
    ap.add_argument("--rollup", required=True, help="perf_ab_rollup --json output")
    ap.add_argument("--title", default="A/B (base -> head)")
    ap.add_argument("--out", default="-", help="output .md path ('-' = stdout)")
    ap.add_argument("--topn", type=int, default=8)
    args = ap.parse_args()

    # --- kernel tier: per-pattern speedup from the raw sweeps ---
    B, H = _sweep_points(args.base), _sweep_points(args.head)
    ks = set(B) & set(H)
    byfam = defaultdict(list)
    for k in ks:
        byfam[k[0]].append(B[k] / H[k])
    fam_geo = {f: statistics.geometric_mean(v) for f, v in byfam.items() if v}
    corpus_k = statistics.geometric_mean([B[k] / H[k] for k in ks]) if ks else float("nan")

    # --- model/suite tiers from the rollup (genai excluded) ---
    r = json.load(open(args.rollup))
    pm_all = {k: v for k, v in (r.get("per_model", r)).items() if v is not None}
    pm = {k: v for k, v in pm_all.items() if "genai" not in k.split("/")[0]}
    genai = {k: v for k, v in pm_all.items() if "genai" in k.split("/")[0]}
    by_sm = defaultdict(list)
    for k, v in pm.items():
        p = k.split("/")
        if len(p) >= 3:
            by_sm[f"{p[0]}/{p[1]}"].append(v)

    L = []
    L.append(f"# Perf highlights — {args.title}\n")
    L.append("Widest to narrowest; each tier in its OWN unit. Model/suite/all-models")
    L.append("numbers EXCLUDE genai (single-kernel microbenchmarks, not models); genai's")
    L.append("kernel wins are in the kernel tier. Kernel != model: a big kernel speedup is")
    L.append("diluted at the model level by the conv/GEMM/SDPA time the change never touches.")
    L.append("Generated by scripts/gen_highlights.py.\n")

    L.append("## Tier 4 — All models (headline)")
    L.append(f"**{statistics.median(list(pm.values())):+.2f}% median / "
             f"{_geo_pct(list(pm.values())):+.2f}% geomean** per-model end-to-end "
             f"across {len(pm)} models (genai-excluded).\n")

    L.append("## Tier 3 — Suites (per-model-e2e geomean within each suite×mode)")
    L.append("| suite / mode | e2e geomean | median | n |")
    L.append("|---|--:|--:|--:|")
    for sm in sorted(by_sm):
        L.append(f"| {sm} | {_geo_pct(by_sm[sm]):+.2f}% | "
                 f"{statistics.median(by_sm[sm]):+.2f}% | {len(by_sm[sm])} |")

    L.append("\n## Tier 2 — Individual models (per-model end-to-end)")
    L.append("| e2e Δ | model |")
    L.append("|------:|-------|")
    for k, v in sorted(pm.items(), key=lambda kv: -kv[1])[:args.topn]:
        L.append(f"| {v:+.1f}% | `{k}` |")

    L.append("\n## Tier 1 — Kernels (speedup of the fused kernel itself; NOT a model speedup)")
    L.append(f"Corpus kernel-geomean **{corpus_k:.3f}×** over {len(ks)} shape points. "
             f"Top patterns (each the speedup of that fused kernel; some are single shape "
             f"points, not multi-sample aggregates):\n")
    L.append("| speedup | kernel pattern |")
    L.append("|--------:|----------------|")
    for f in sorted(fam_geo, key=lambda f: -fam_geo[f])[:args.topn]:
        L.append(f"| {fam_geo[f]:.1f}× | `{f}` |")
    if genai:
        L.append("\n_genai single-kernel workloads (kernel-tier, excluded from model geomeans):_ "
                 + ", ".join(f"{k.split('/')[-1]} {v:+.0f}%"
                             for k, v in sorted(genai.items(), key=lambda kv: -kv[1])[:4]))

    txt = "\n".join(L) + "\n"
    if args.out == "-":
        print(txt)
    else:
        open(args.out, "w").write(txt)
        print(f"wrote {args.out}")


if __name__ == "__main__":
    main()
