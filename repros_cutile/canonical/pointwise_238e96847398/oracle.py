@ct.kernel
def _copy_bf16_kernel(
    src_ptr,
    dst_ptr,
    N: ct.Constant[int],
    BLOCK: ct.Constant[int],
):
    pid = ct.bid(0)
    x = ct.load(src_ptr, index=(pid,), shape=(BLOCK,),
                padding_mode=ct.PaddingMode.ZERO)
    y = ct.astype(ct.astype(x, ct.float32), ct.bfloat16)
    ct.store(dst_ptr, index=(pid,), tile=y)


